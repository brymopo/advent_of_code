formatted_input = puzzle_input.split('\n')

def get_index(position,length):
    return position % length

def traverse_map(patterns,x,y):
    trees = 0
    position_x = x
    position_y = y
    while position_y < len(patterns):
        current_pattern = patterns[position_y]
        index = get_index(position_x,len(current_pattern))

        if current_pattern[index] == '#':
            trees += 1

        position_x += x
        position_y += y
    return trees
        
